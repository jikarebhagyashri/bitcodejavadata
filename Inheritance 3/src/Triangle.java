public final class Triangle extends Shape{

    private  int w;
    private  int h;

    public  Triangle(){
        this.h = 0;
        this.w = 0;
    }

    public Triangle(int h, int w){
        this.h = 0;
        this.w = 0;
    }
    public int getH(){
        return h;
    }
    public void setH(int h){
        this.h = h;
    }
    public int getW(){
        return w;
    }
    public void setW(int w){
        this.h = h;
    }
}
